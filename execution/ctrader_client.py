    def has_open_position(self, symbol: str) -> bool:
        with self._lock:
            return symbol in self._positions

    def test_connection(self) -> bool:
        """Verify credentials/connectivity without placing any order."""
        try:
            logger.info("🧪 Testing cTrader connection...")
            if not self.connect(timeout=10.0):
                logger.error("❌ Connection test failed")
                return False
            info = self.get_account_info()
            if info:
                logger.info(
                    f"✅ cTrader test PASSED | balance={info.balance} {info.currency} "
                    f"leverage=1:{info.leverage} margin_free={info.margin_free}"
                )
                return True
            logger.error("❌ Could not fetch account info")
            return False
        except Exception as exc:
            logger.error(f"❌ Connection test failed: {exc}")
            return False

    def disconnect(self) -> None:
        """Cleanly stop the client service (the shared reactor keeps running)."""
        if self._client:
            try:
                from twisted.internet import reactor
                if reactor.running:
                    logger.debug("Stopping cTrader client service...")
                    reactor.callFromThread(self._client.stopService)
            except Exception as exc:
                logger.warning(f"Error during disconnect: {exc}")
        self._set_state(ConnectionState.DISCONNECTED)
        logger.info("cTrader disconnected")
