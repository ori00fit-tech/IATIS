"""
execution/ctrader_client.py
----------------------------
IC Markets / cTrader Open API client for IATIS (v4 — corrected).

Protocol : TCP with Protobuf messages (NOT REST)
Library  : ctrader-open-api 0.9.2+

WHAT CHANGED vs v3 (all fixes verified against the real protobuf schema
generated by `ctrader-open-api`):

  CRITICAL
    C1. place_market_order() no longer reads a non-existent
        `CTraderOrder.entry_price`. It sends ABSOLUTE stopLoss / takeProfit
        (ProtoOANewOrderReq.stopLoss / .takeProfit are doubles), so no entry
        reference is required for a market order.
    C2. Execution response is parsed correctly. The reply to
        ProtoOANewOrderReq is a ProtoOAExecutionEvent whose ids/prices live
        under .deal / .position / .order — NOT at the top level. executionPrice
        is a real price and is NOT divided by 100_000.
    C3. Order volume is converted from lots to the API's volume unit using the
        symbol's real `lotSize` (fetched via ProtoOASymbolByIdReq), then snapped
        to stepVolume and clamped to [minVolume, maxVolume]. Passing "centi-lots"
        directly (the old bug) sent a near-zero volume that the broker rejects.

  MAJOR
    M1. READY-state race fixed: whichever of {trader info, symbols list} arrives
        last triggers the READY transition via _maybe_ready().
    M2. Account info uses fields that actually exist on ProtoOATrader
        (balance + moneyDigits, leverageInCents). Non-existent fields
        (depositAsset.name, marginUsed, freeMargin) are no longer referenced.
    M3. Position tracking is populated from ProtoOAExecutionEvent (the real
        source of position lifecycle) instead of dead PositionOpen/Close handlers.

  MINOR
    - Removed unused imports; added a lock around shared state; set both
      `label` and `comment`; documented single-reactor-per-process behaviour.

Connection state flow:
  DISCONNECTED → TCP_CONNECTED → APP_AUTH_OK → ACCOUNT_AUTH_OK
              → SYMBOLS_LOADED → READY
"""
